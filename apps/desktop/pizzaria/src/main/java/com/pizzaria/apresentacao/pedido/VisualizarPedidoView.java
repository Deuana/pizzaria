    private void carregarPedidos(){
         PedidoRN pedidoRN = new PedidoRN();
         PedidoTableModel modelo = new PedidoTableModel();
         try{
            for (BaseDTO b : pedidoRN.obterPedidosPendentes()) {
               modelo.addPedido(((PedidoDTO) b));
            }
          } catch (Exception e) {
            e.printStackTrace();
            //JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção",  JOptionPane.INFORMATION_MESSAGE);
        }

        tblPedidos.setModel(modelo);
    }
