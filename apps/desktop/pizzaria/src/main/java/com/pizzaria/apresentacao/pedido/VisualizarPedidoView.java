    private void btnEncaminharMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEncaminharMouseClicked

           PedidoRN pedidoRN = new PedidoRN();
            
            try{
                for (Integer index : tblPedidos.getSelectedRows()) {                    
                    PedidoDTO p = ((PedidoTableModel) tblPedidos.getModel()).getPedido(index);
                    Timestamp datahoje = new Timestamp(System.currentTimeMillis());
                    p.setPedidoPronto(Boolean.TRUE);
                    p.setTimeInsert(datahoje);
                    pedidoRN.atualizarPedido(p);
                }
            carregarPedidos();
            }catch(Exception e){
                 e.printStackTrace();
                JOptionPane.showMessageDialog(null, e.getMessage(), "Atenção",  JOptionPane.INFORMATION_MESSAGE);
            }
                    
        
    }//GEN-LAST:event_btnEncaminharMouseClicked


